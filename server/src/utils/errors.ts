// utils/errors.ts — Clases de error personalizadas para manejar errores de forma consistente
//
// AppError: error base con código HTTP y mensaje
// NotFoundError: para recursos no encontrados (404)
// UnauthorizedError: para acceso no autorizado (401)
// ForbiddenError: para acciones prohibidas (403)
// ValidationError: para datos inválidos (400)

export class AppError extends Error {
  constructor(
    public statusCode: number,
    message: string
  ) {
    super(message);
    this.name = "AppError";
  }
}

export class NotFoundError extends AppError {
  constructor(message = "Resource not found") {
    super(404, message);
    this.name = "NotFoundError";
  }
}

export class UnauthorizedError extends AppError {
  constructor(message = "Unauthorized") {
    super(401, message);
    this.name = "UnauthorizedError";
  }
}

export class ForbiddenError extends AppError {
  constructor(message = "Forbidden") {
    super(403, message);
    this.name = "ForbiddenError";
  }
}

export class ConflictError extends AppError {
  constructor(message = "Conflict") {
    super(409, message);
    this.name = "ConflictError";
  }
}